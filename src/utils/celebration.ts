export function launchEmojiBurst(emojis: string[]) {
  const sides = [
    { angle: 60, origin: { x: 0, y: 0.5 } },
    { angle: 120, origin: { x: 1, y: 0.5 } },
  ];

  sides.forEach((side) => {
    emojis.forEach((emoji) => {
      confetti({
        ...side,
        particleCount: 30,
        spread: 60,
        ticks: 120,
        scalar: 1.8,
        shapes: ["text"],
        text: "🎉",
      } as any); // bypass TS shape limitation
    });
  });
}
